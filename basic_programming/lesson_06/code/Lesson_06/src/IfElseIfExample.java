public class IfElseIfExample {

    public static void main(String[] args) {
        // if - if-else - else
        int x = 1;
        if (x > 10) {
            System.out.println("x is greater than 10");
        } else if (x > 5) {
            System.out.println("x is greater than 5 but not greater than 10");
        } else {
            System.out.println("Output");
        }
    }
}
